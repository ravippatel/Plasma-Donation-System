    public void popAssociationComboBox() {
        associationJComboBox.removeAllItems();

        for (Association a : ent.getAssociationDirectory().getAssociationList()) {
            associationJComboBox.addItem(a);
        }
    }

    public void populateEmployeeComboBox(Association organization) {
        employeeJComboBox.removeAllItems();

        for (Employee employee : organization.getEmpDir().getEmpList()) {
            employeeJComboBox.addItem(employee);
        }
    }

    private void populateRoleComboBox(Association organization) {
        roleJComboBox.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            roleJComboBox.addItem(role);
        }
    }

    public void popDataTable() {

        DefaultTableModel model = (DefaultTableModel) userJTable.getModel();

        model.setRowCount(0);

        for (Association organization : ent.getAssociationDirectory().getAssociationList()) {
            for (User_Acc ua : organization.getUserAccDir().getUser_Account_List()) {
                Object row[] = new Object[3];
                row[0] = ua.getEmployee();
                row[1] = ua.getRole();
                row[2] = ua;
                ((DefaultTableModel) userJTable.getModel()).addRow(row);
            }
        }
    }

