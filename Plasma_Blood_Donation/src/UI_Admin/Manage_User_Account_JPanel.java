    private void create_User(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_User

        //validate all the selected fields if not selected
        if (associationJComboBox.getSelectedItem().equals(null)) {
            associationJComboBox.setBorder(BorderFactory.createLineBorder(Color.RED));
            associationJComboBox.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Please select an Organization");
        } else if (employeeJComboBox.getSelectedItem().equals(null)) {
            employeeJComboBox.setBorder(BorderFactory.createLineBorder(Color.RED));
            employeeJComboBox.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Please select an Employee");
        } else if (roleJComboBox.getSelectedItem().equals(null)) {
            roleJComboBox.setBorder(BorderFactory.createLineBorder(Color.RED));
            roleJComboBox.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Please select a Role");
        } else if (nameJTextField.getText().isEmpty()) {
            nameJTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
            nameJTextField.setForeground(Color.red);

            JOptionPane.showMessageDialog(null, new JLabel("Usename cannot be blank!!!"), "Error", JOptionPane.ERROR_MESSAGE);
        } else if (passwordJTextField.getText().length() < 4) {
            passwordJTextField.setBorder(BorderFactory.createLineBorder(Color.RED));
            passwordJTextField.setForeground(Color.red);

            JOptionPane.showMessageDialog(null, new JLabel("Password should atleast have 4 digits!!!!!"), "Error", JOptionPane.ERROR_MESSAGE);

        } else if (!check_UserIdExists(nameJTextField.getText())) {

            JOptionPane.showMessageDialog(null, new JLabel("Username already exits!!!!!!"), "Error", JOptionPane.ERROR_MESSAGE);

        } 
        //create and add user
        else {
            String userName = nameJTextField.getText();
            String pass = passwordJTextField.getText();
            Association a = (Association) associationJComboBox.getSelectedItem();
            Employee employee = (Employee) employeeJComboBox.getSelectedItem();
            Role role = (Role) roleJComboBox.getSelectedItem();
            a.getUserAccDir().create_User_Account(userName, pass, employee, role);
            popDataTable();
            JOptionPane.showMessageDialog(null, new JLabel("Successfully created user account!!!!!"));//, "Error", JOptionPane.ERROR_MESSAGE);
            dB4OUtil.storeSystem(sys);
            nameJTextField.setText("");
            passwordJTextField.setText("");
        }

    }//GEN-LAST:event_create_User
    //check if the user exists
    private boolean check_UserIdExists(String userName) {
        if (sys.getUserAccDir().check_If_Username_Is_Unique(userName)) {
            if (sys.getUserAccDir().check_If_Username_Is_Unique(userName)) {
                for (Network_Cities network : sys.getCityList()) {
                    for (Enterprise enterprise : network.getEnterprise_Dir().getEnt_List()) {
                        if (enterprise.getUserAccDir().check_If_Username_Is_Unique(userName)) {
                            if (enterprise.getAssociationDirectory().getAssociationList().size() > 0) {
                                for (Association a : enterprise.getAssociationDirectory().getAssociationList()) {
                                    if (a.getUserAccDir().check_If_Username_Is_Unique(userName)) {
                                        return true;
                                    } else {
                                        return false;
                                    }
                                }
                            } else {
                                return true;
                            }
                        } else {
                            return false;
                        }
                    }
                }
            } else {
                return false;
            }
        } else {
            return false;
        }
        return false;
    }
