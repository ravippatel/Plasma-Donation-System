    //used to opulate combo box
    private void populateCombo() {
        organizationJComboBox.removeAllItems();
        System.out.println(ent.getEnterprise_type());
        System.out.println(ent.getEnterprise_type().toString().equals(PlasmaBank.toString()));
        if (ent.getEnterprise_type().toString().equals(Legal.toString())) {
            for (Association.LegalType legalType : Association.LegalType.values()) {
                if (legalType.getValue().equals(Association.LegalType.LegalOfficer.getValue())) {
                    organizationJComboBox.addItem(legalType);
                }
            }
        } else if (ent.getEnterprise_type().toString().equals(PlasmaBank.toString())) {
            System.out.println("1");
            for (Association.PlasmaBankType plasmaType : Association.PlasmaBankType.values()) {
                System.out.println("1");
                System.out.println(plasmaType.getValue());
                System.out.println(Association.PlasmaBankType.PlasmaBank.getValue());
                if (plasmaType.getValue().equals(Association.PlasmaBankType.PlasmaBank.getValue())) {
                    organizationJComboBox.addItem(plasmaType);
                }
            }
        } else if (ent.getEnterprise_type().toString().equals(CovidCentre.toString())) {
            for (Association.CovidCentreType covidType : Association.CovidCentreType.values()) {
                if (covidType.getValue().equals(Association.CovidCentreType.CovidCentre.getValue())) {
                    organizationJComboBox.addItem(covidType);
                }
            }
        } else if (ent.getEnterprise_type().toString().equals(Government.toString())) {
            for (Association.GovernmentType govtType : Association.GovernmentType.values()) {
                if (govtType.getValue().equals(Association.GovernmentType.Government.getValue())) {
                    organizationJComboBox.addItem(govtType);
                }
            }
        } else {
            for (Association.Type type : Association.Type.values()) {
                if (type.getValue().equals(Association.Type.SystemCoordinator.getValue())
                        || type.getValue().equals(Association.Type.Doctor.getValue())
                        || type.getValue().equals(Association.Type.Pathologist.getValue())) {
                    organizationJComboBox.addItem(type);
                }
            }

        }
    }

    //used to populate table
    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        model.setRowCount(0);
        for (Association organization : dir.getAssociationList()) {
            Object[] row = new Object[2];
            row[0] = organization.getName();
            row[1] = organization.getFirstName();
            model.addRow(row);
        }
    }

