    private void add_association(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_association

        String check = "";

        if (txtOrgRealName.getText().isEmpty()) {
            txtOrgRealName.setBorder(BorderFactory.createLineBorder(Color.RED));
            txtOrgRealName.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, "Association name cannot be empty!");
            check = "do not go further!";
            return;
        }

        for (Association association : dir.getAssociationList()) {

            if (association.getName().equals(txtOrgRealName.getText())) {

                JOptionPane.showMessageDialog(null, "Association name already exists!");
                check = "do not go further!";
                return;
            }
        }

        // Main Process after validation checks
        if (check.equals("")) {
            if (ent.getEnterprise_type().toString().equals(Legal.toString())) {
                dir.createLegalAssociation((LegalType) organizationJComboBox.getSelectedItem(), txtOrgRealName.getText());
            } else if (ent.getEnterprise_type().toString().equals(Government.toString())) {
                dir.createGovernmentAssociation((GovernmentType) organizationJComboBox.getSelectedItem(), txtOrgRealName.getText());
            } else if (ent.getEnterprise_type().toString().equals(PlasmaBank.toString())) {
                dir.createPlasmaAssociation((PlasmaBankType) organizationJComboBox.getSelectedItem(), txtOrgRealName.getText());
            } else if (ent.getEnterprise_type().toString().equals(CovidCentre.toString())) {
                dir.createCOVIDAssociation((CovidCentreType) organizationJComboBox.getSelectedItem(), txtOrgRealName.getText());
            } else {

                Type type = (Type) organizationJComboBox.getSelectedItem();
                dir.createAssociation(type, txtOrgRealName.getText());
            }
            //if conditions match then populate and display into table
            populateTable();
            JOptionPane.showMessageDialog(null, "Association created successfully!");
            dB4OUtil.storeSystem(sys);

            txtOrgRealName.setText("");
            organizationJComboBox.removeItem(organizationJComboBox.getSelectedItem());
        }
    }//GEN-LAST:event_add_association

