    private void create_admin(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_admin

        int selectedRow = enterprise_JTable.getSelectedRow();

        if (txt_AdminName.getText().isEmpty()) {
            txt_AdminName.setBorder(BorderFactory.createLineBorder(Color.RED));
            txt_AdminName.setForeground(Color.red);
        }

        if (txt_UserName.getText().isEmpty()) {
            txt_UserName.setBorder(BorderFactory.createLineBorder(Color.RED));
            txt_UserName.setForeground(Color.red);
        }

        if (txt_Password.getText().isEmpty()) {
            txt_Password.setBorder(BorderFactory.createLineBorder(Color.RED));
            txt_Password.setForeground(Color.red);
        }

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, new JLabel("<html><h2><I>Please select<font color='red'> a row</font> to process! </I></h2></html>"), "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (txt_UserName.getText().isEmpty() || txt_AdminName.getText().isEmpty() || txt_Password.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, new JLabel("<html><h2><I>All fields are</I><font color='red'> mandatory</font>!</h2></html>"), "Error", JOptionPane.ERROR_MESSAGE);
        } else if (!check_UserId_Exists(txt_UserName.getText())) {
            txt_UserName.setBorder(BorderFactory.createLineBorder(Color.RED));
            txt_UserName.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, new JLabel("<html><h2><I>User name</I><font color='red'> already</font> present!</h2></html>"), "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (txt_Password.getText().length() < 4) {
            txt_Password.setBorder(BorderFactory.createLineBorder(Color.RED));
            txt_Password.setForeground(Color.red);
            JOptionPane.showMessageDialog(null, new JLabel("<html><h2><font color='red'><I>Password</I></font> should be<font color='green'> more than 4 digit!</font>!</h2></html>"), "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            String uname = txt_UserName.getText();
            String pass = String.valueOf(txt_Password.getPassword());

            Employee employee = enterprise.getEmpDir().createEmp(txt_AdminName.getText());

            User_Acc account = enterprise.getUserAccDir().create_User_Account(uname, pass, employee, new Admin());
            populate_AdminTable(enterprise);

            populate_AdminTable(enterprise);

            JOptionPane.showMessageDialog(null, new JLabel("<html><h2>New Admin<font color='green'><I> credentials</I></font> created!</h2></html>"));

            dB4OUtil.storeSystem(system);

            txt_AdminName.setText("");
            txt_UserName.setText("");
            txt_Password.setText("");
            txt_Name.setText("");
            txt_AdminName.setEnabled(false);
            txt_UserName.setEnabled(false);
            txt_Password.setEnabled(false);
            txt_Name.setEnabled(false);
        }


    }//GEN-LAST:event_create_admin

    private void enterprise_JTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enterprise_JTableMouseClicked
        int row = enterprise_JTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, new JLabel("<html><h2><I>Please select<font color='red'> a row</font> from the<font color='green'> Table</I></font></h2></html>"), "Warning", JOptionPane.WARNING_MESSAGE);

            return;
        }
        Enterprise enterprise2 = (Enterprise) enterprise_JTable.getValueAt(row, 0);
        enterprise = enterprise2;

        txt_Name.setText(enterprise2.getName());
        txt_AdminName.setEnabled(true);
        txt_UserName.setEnabled(true);
        txt_Password.setEnabled(true);

        populate_AdminTable(enterprise2);

    }//GEN-LAST:event_enterprise_JTableMouseClicked

