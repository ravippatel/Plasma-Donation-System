    private boolean check_UserId_Exists(String userName) {
        if (system.getUserAccDir().check_If_Username_Is_Unique(userName)) {
            if (system.getUserAccDir().check_If_Username_Is_Unique(userName)) {
                for (Network_Cities network : system.getCityList()) {
                    for (Enterprise enterprise : network.getEnterprise_Dir().getEnt_List()) {
                        if (enterprise.getUserAccDir().check_If_Username_Is_Unique(userName)) {
                            if (enterprise.getAssociationDirectory().getAssociationList().size() > 0) {
                                for (Association organization : enterprise.getAssociationDirectory().getAssociationList()) {
                                    if (organization.getUserAccDir().check_If_Username_Is_Unique(userName)) {
                                        return true;

                                    } else {
                                        return false;
                                    }

                                }
                            } else {
                                return true;
                            }
                        } else {
                            return false;

                        }
                    }
                }
            } else {
                return false;
            }
        } else {
            return false;
        }
        return false;
    }

    private void populate_Table() {
        DefaultTableModel table = (DefaultTableModel) enterprise_JTable.getModel();

        table.setRowCount(0);
        for (Network_Cities city : system.getCityList()) {
            for (Enterprise enterprise : city.getEnterprise_Dir().getEnt_List()) {

                Object[] row = new Object[6];
                row[0] = enterprise;
                row[1] = enterprise.getEnterprise_type().getValue();
                row[2] = city.getCity_name();
                row[5] = enterprise.getEmail();
                row[3] = enterprise.getMobile();
                row[4] = enterprise.getPincode();

                table.addRow(row);

            }
        }
    }

    private void populate_AdminTable(Enterprise enterprise1) {

        DefaultTableModel table = (DefaultTableModel) tbl_AdminUserName.getModel();

        table.setRowCount(0);

        for (User_Acc user : enterprise1.getUserAccDir().getUser_Account_List()) {
            Object[] row = new Object[3];
            row[0] = enterprise;
            row[1] = user.getEmployee().getName();
            row[2] = user.getUsername();
            table.addRow(row);
        }
    }

