    private void view_details(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_view_details
        // TODO add your handling code here:
        int selectedRow = requestTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, new JLabel("Please select row from the table to view details!!!!!!"), "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            //display the details 
            view_Request_Details((Donor_Req) requestTable.getValueAt(selectedRow, 0));
            button_Approve.setEnabled(true);
            button_Reject.setEnabled(true);

            if (Integer.parseInt(ageText.getText()) < 18) // check if age is less than 18 years
            {
                ageText.setBorder(BorderFactory.createLineBorder(Color.RED));
                ageText.setForeground(Color.red);
            }
            if (labConfirmationText.getText().equals("false")) // Was your COVID diagnosis confirmed by a lab test?
            {
                labConfirmationText.setBorder(BorderFactory.createLineBorder(Color.RED));
                labConfirmationText.setForeground(Color.red);
            }
            if (followUpTest.getText().equals("false")) // did you have a follow up test that was negative for COVID?
            {
                followUpTest.setBorder(BorderFactory.createLineBorder(Color.RED));
                followUpTest.setForeground(Color.red);
            }
            if (symptomsText.getText().equals("false")) // Currently you do not have any symptoms? 
            {
                symptomsText.setBorder(BorderFactory.createLineBorder(Color.RED));
                symptomsText.setForeground(Color.red);
            }
            if (lastDaySymptomsText.getText().equals("false")) // Has it been at least 14 days since the last day of your symptoms?
            {
                lastDaySymptomsText.setBorder(BorderFactory.createLineBorder(Color.RED));
                lastDaySymptomsText.setForeground(Color.red);
            }
            if (new Date().getDay() - lastDonationDateField.getDate().getDay() < 30) // check if age is less than 30 years
                            {
                lastDonationDateField.setBorder(BorderFactory.createLineBorder(Color.RED));
                lastDonationDateField.setForeground(Color.red);
            }
            dB4OUtil.storeSystem(system);

        }
    }

