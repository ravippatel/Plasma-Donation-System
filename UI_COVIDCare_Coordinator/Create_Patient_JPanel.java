    //creates new patient
    private void create_patient(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_create_patient
        // TODO add your handling code here:
        Patient patient = system.getPatientDir().addPatient();
        patient.setReceiverID(uidTextField.getText());
        patient.setName(nameTextField.getText());
        patient.setEmailID((emailTextField.getText()));
        patient.setBlood_Group(bloodGroupTextField.getText());
        patient.setEmergency_Status(chkEmergency.isSelected());
        patient.setStatus("Centre Approved");
        populateTable();
        JOptionPane.showMessageDialog(null, new JLabel("<html><h2><I>A new Patinet has been <font color='green'>added</font></I></h2></html>"));
        Work_Req request = new System_Coordinator_Work_Request();
        request.setPatient(patient);
        request.setAction_Date(new Date());
        request.setAssigned("Legal Pool");
        request.setSummary("Requested for Plasma Reception");
        request.setStatus("Assigned to Legal Pool");
        request.setUser_Acc(user_Account);
        Enterprise ent = null;
        Association org = null;

        for (Enterprise enterprise : network.getEnterprise_Dir().getEnt_List()) {
            if (enterprise.getEnterprise_type().toString().equals("Legal")) {

                ent = enterprise;
                System.out.println(enterprise);
                break;
            }
        }

        for (Association a : ent.getAssociationDirectory().getAssociationList()) {
            if (a instanceof Legal_Officer_Association) {
                org = a;
                break;
            }
        }

        if (org != null) {
            org.getWorkQueue().getWork_Req_List().add(request);
            System.out.println(org.getName());
            user_Account.getWorkQueue().getWork_Req_List().add(request);

            dB4OUtil.storeSystem(system);
            populateTable();
            JOptionPane.showMessageDialog(null, new JLabel("Request has been transferred for Legal Approval!!!!"));

        } else {

            JOptionPane.showMessageDialog(null, "No organization present", "Error", JOptionPane.ERROR_MESSAGE);
        }
        chkEmergency.setEnabled(false);
        btnCreateReceiver.setEnabled(false);
        uidTextField.setText("");
        nameTextField.setText("");
        emailTextField.setText("");
        bloodGroupTextField.setText("");
    }//GEN-LAST:event_create_patient
    
