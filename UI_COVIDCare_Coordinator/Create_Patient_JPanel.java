    private void populatePatientTable() {
        DefaultTableModel dtmA = (DefaultTableModel) tblPatient.getModel();
        dtmA.setRowCount(0);
        Object row[] = new Object[5];
        String line = "";
        String splitBy = ",";
        try {
            //parsing a CSV file into BufferedReader class constructor  
            BufferedReader br = new BufferedReader(new FileReader("RecordforPatients.csv"));
            while ((line = br.readLine()) != null) //returns a Boolean value  
            {
                String[] csv = line.split(splitBy);    // use comma as separator 
                row[0] = csv[0];
                row[1] = csv[1];
                row[2] = csv[2];
                row[3] = csv[3];
                row[4] = csv[4];

                dtmA.addRow(row);
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

    }

    private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblRequest.getModel();

        dtm.setRowCount(0);

        for (Work_Req request : user_Account.getWorkQueue().getWork_Req_List()) {
            Object[] row = new Object[6];
            row[0] = request;
            row[1] = request.getSummary();
            row[2] = request.getPatient();
            row[3] = request.getPatient().getEmailID();
            row[4] = request.getStatus();
            row[5] = request.getAction_Date();
            dtm.addRow(row);
        }
    }

